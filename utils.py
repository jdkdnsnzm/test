# utils.py - 工具函数模块 
 
class Calculator: 
    """计算器类""" 
 
    @staticmethod 
    def add(a, b): 
        """加法运算""" 
        return a + b 
 
    @staticmethod 
    def subtract(a, b): 
        """减法运算""" 
        return a - b 
 
    @staticmethod 
    def multiply(a, b): 
        """乘法运算""" 
        return a * b 
